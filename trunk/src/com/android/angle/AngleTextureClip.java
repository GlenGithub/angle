package com.android.angle;

public class AngleTextureClip
{
	public int mID;
	public int mLeft;
	public int mTop;
	public int mWidth;
	public int mHeight;

	AngleTextureClip(int id, int left, int top, int width, int height)
	{
		mID = id;
		mLeft = left;
		mTop = top;
		mWidth = width;
		mHeight = height;
	}
}
